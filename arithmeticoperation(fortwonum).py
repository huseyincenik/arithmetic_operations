# perform arithmetic operation on two number

a = int(input("Enter one number : "))
b = int(input("Enter 2nd numbers : "))

print("Addition of two number = ",a + b)
print("Subtraction of two number = ",a - b)
print("Multiplication of two number = ",a * b)
print("Division of two number = ",a / b) #float division
print("Floor division of two number = ",a // b) #integer division
print("Mod of two number = ",a % b)